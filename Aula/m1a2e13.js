let lado = 5, largura = 16, comprimento = 6, base = 12, altura = 8, resposta = 0;

//Área do Quadrado = lado vezes lado
resposta = lado * lado;
console.log(`A área do quadrado é ${resposta}`);

//Área do Retângulo = largura vezes comprimento
resposta = largura * comprimento;
console.log(`A área do retângulo é ${resposta}`);

//Área do Triângulo = base vezes altura dividido por 2
resposta = (base * altura) / 2;
console.log(`A área do Triângulo é ${resposta}`);